#include "3-calc.h"
/**
* op_add - sum of a and b.
* @a: integer.
* @b: integer.
* Return: result.
*/

int op_add(int a, int b)
{
	return (a + b);
}



/**
* op_sub - difference of a and b.
* @a: integer.
* @b: integer.
* Return: result.
*/

int op_sub(int a, int b)
{
	return (a - b);
}



/**
* op_mul - product of a and b.
* @a: integer.
* @b: integer.
* Return: result.
**/

int op_mul(int a, int b)
{
	return (a * b);
}



/**
* op_div - divides a from b.
* @a: integer.
* @b: integer.
* Return: result.
*/

int op_div(int a, int b)
{
	return (a / b);
}



/**
* op_mod - mod a from b
* @a: integer.
* @b: integer.
* Return: result.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
